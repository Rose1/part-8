const typeDefs = gql`
  type Author {
    name: String!
    born: Int
    id: ID!
  }
  type Book {
    title: String!
    published: Int!
    authorName: Author
    genres: [String!]!
    id: ID!
  }
  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks: [Book!]!
  }
`

const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allBooks: () => books
  },
  Book: {
    authorName: (root) => {
      return authors.find(a => a.name === root.authorName)
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
